package coreJavaBasics.day1Lab1;

public class JVMArchitecture {
	public static void main(String[] args) {
		System.out.println("1. JVM (Java Virtual Machine) is the runtime environment for Java bytecode.");
		System.out.println("2. JVM is the heart of Java technology, interpreting Java bytecode into machine code.");
		System.out.println("3. JVM provides a sandboxed environment for Java code to run, ensuring security and platform independence.");
		System.out.println("4. JVM consists of three main components: Class Loader, Runtime Data Areas, and Execution Engine.");
		System.out.println("5. Class Loader loads and links Java classes into the JVM.");
		System.out.println("6. Runtime Data Areas provide memory for storing data and code during execution.");
		System.out.println("7. Execution Engine executes Java bytecode, using interpreters or just-in-time (JIT) compilers.");
		System.out.println("8. JVM provides various runtime data areas, including the PC Register, JVM Stack, and Native Method Stack.");
		System.out.println("9. The PC Register stores the current instruction address being executed.\r\n" + "");
		System.out.println("10. JVM Stack stores local variables, method invocation, and return values.\r\n" + "");
		System.out.println("11. Native Method Stack stores native method information and return values.");
		System.out.println("12. JVM uses garbage collection to automatically manage memory and avoid memory leaks.");
		System.out.println("13. JVM provides security features, such as bytecode verification and sandboxing, to ensure secure execution.");
		System.out.println("14. JVM supports multithreading, allowing concurrent execution of Java programs.");
		System.out.println("15. JVM is an essential component of Java technology, enabling platform-independent execution of Java code.");

	}
}
